public class ModeItemInfo {
    private String name;
    private int image_click;
    private int image_no_focus;
    private int image_focused;
    private String image_click_Str;
    private String image_no_focus_Str;
    private String image_focused_Str;

    private int value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage_click() {
        return image_click;
    }

    public void setImage_click(int image_click) {
        this.image_click = image_click;
    }

    public int getImage_no_focus() {
        return image_no_focus;
    }

    public void setImage_no_focus(int image_no_focus) {
        this.image_no_focus = image_no_focus;
    }

    public int getImage_focused() {
        return image_focused;
    }

    public void setImage_focused(int image_focused) {
        this.image_focused = image_focused;
    }




    public String getImage_click_Str() {
        return image_click_Str;
    }

    public void setImage_click_Str(String image_click_Str) {
        this.image_click_Str = image_click_Str;
    }

    public String getImage_no_focus_Str() {
        return image_no_focus_Str;
    }

    public void setImage_no_focus_Str(String image_no_focus_Str) {
        this.image_no_focus_Str = image_no_focus_Str;
    }

    public String getImage_focused_Str() {
        return image_focused_Str;
    }

    public void setImage_focused_Str(String image_focused_Str) {
        this.image_focused_Str = image_focused_Str;
    }




    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
