public class ReportFromBaseBean {

    protected String compid;
    protected String componenttype;
    protected String compsubtitle;
    protected String compsubid;

    public String getCompid() {
        return compid;
    }

    public void setCompid(String compid) {
        this.compid = compid;
    }

    public String getComponenttype() {
        return componenttype;
    }

    public void setComponenttype(String componenttype) {
        this.componenttype = componenttype;
    }

    public String getCompsubtitle() {
        return compsubtitle;
    }

    public void setCompsubtitle(String compsubtitle) {
        this.compsubtitle = compsubtitle;
    }

    public String getCompsubid() {
        return compsubid;
    }

    public void setCompsubid(String compsubid) {
        this.compsubid = compsubid;
    }
}
